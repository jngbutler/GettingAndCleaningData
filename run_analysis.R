
## Step 1: Merge the training and test sets to create one data set

## read training data 
subject_train <- read.table("./UCI HAR Dataset/train/subject_train.txt")
x_train <- read.table("./UCI HAR Dataset/train/X_train.txt")
y_train <- read.table("./UCI HAR Dataset/train/y_train.txt")
train <- cbind(subject_train,x_train,y_train)  ## combine training files into one data set

## read test data 
subject_test <- read.table("./UCI HAR Dataset/test/subject_test.txt")
x_test <- read.table("./UCI HAR Dataset/test/X_test.txt")
y_test <- read.table("./UCI HAR Dataset/test/y_test.txt")
test <- cbind(subject_test,x_test,y_test)  ## combine test files into one data set

## combine training and test data into one data set
data <- rbind(train,test)  

## rename variables using names from features.txt
features <- read.table("./UCI HAR Dataset/features.txt")
varnames <- as.character(features[,2])  
colnames(data) <- c("subjectid",varnames,"activityid")


## Step 2: Extract only the variables on the mean and standard deviation of each measurement

## Note: The assignment asks us to extract variables that contain the mean and std of each measurement.  Some 
## may argue that this only includes variables that contain "mean()" or "std()".  For purposes of this assignment, 
## I am INCLUDING the 7 MEAN variables such as "fBodyAcc-meanFreq()-Z" which is the MEAN of the Frequency of the Body 
## Acceleration signal.  However, I am EXCLUDING the 7 ANGLE variables such as "angle(tBodyAccMean,gravity)" which although 
## it contains the string "mean", is defined in the features_info.txt file as the "ANGLE between [the two] vectors" 
## for Body Acceleration Mean and gravity.

variables <- names(data)            
mydata1 <- data[,variables[which(!grepl("angle",variables) & grepl(".([Mm][Ee][Aa][Nn]|[Ss][Tt][Dd]).",variables))]]
mydata2 <- data[,c(1,563)]
data <- cbind(mydata2,mydata1)


## Step 3: Use descriptive activity names to name the activities in the data set

activity <- read.table("./UCI HAR Dataset/activity_labels.txt")
data <- merge(data, activity, by.x="activityid", by.y="V1", all=FALSE) 
colnames(data)[82] <- "activitylabel"


## Step 4: Appropriately label the data set with descriptive variable names

## clean up names
nicenames <- make.names(names(data))     
nicenames <- gsub("\\.", "", nicenames)         ## remove extra periods
nicenames <- gsub("BodyBody","Body",nicenames)  ## remove "BodyBody" typo
nicenames <- gsub("std","STD",nicenames)
nicenames <- gsub("mean","Mean",nicenames)

## use underscore to separate triaxial notation from rest of string for easier identification
nicenames <- gsub("X","_X",nicenames)  
nicenames <- gsub("Y","_Y",nicenames)
nicenames <- gsub("Z","_Z",nicenames)

colnames(data) <- nicenames


## Step 5: Create tidy data set with the average of each variable for each activity and each subject

## get average for each variable for each activity by subject
tidy_data <- aggregate(data[,3:81], by=list(data$subjectid,data$activitylabel), FUN="mean")

## rename columns
colnames(tidy_data)[1] <- "subjectid"
colnames(tidy_data)[2] <- "activitylabel"
colnames(tidy_data)[3:81] <- paste("AvgOf",colnames(tidy_data)[3:81], sep="_")

